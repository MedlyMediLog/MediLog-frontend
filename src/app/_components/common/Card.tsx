import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "w-full rounded-[20px] bg-[#fbfdfd]",
        className
      )}
      {...props}
    />
  )
)
Card.displayName = "Card"

